    public static bool IsSupported(char c) => Glyphs.ContainsKey(char.ToUpperInvariant(c));

    /// <summary>Returns whether the pixel at (row, col) of the glyph for <paramref name="c"/> is lit.</summary>
    public static bool IsPixelLit(char c, int row, int col)
    {
        var glyph = Glyphs[char.ToUpperInvariant(c)];
        return glyph[row][col] == '#';
    }
